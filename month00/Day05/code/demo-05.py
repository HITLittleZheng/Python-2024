"""
    切片：
        截取连续或一定间隔的字符结果
    语法：
        容器名[[起始索引] : [终止索引] [:步长]]
"""
s = '12345'

# 起始索引为: 终止索引
print(s[:])  # '12345'
print(s[::2])  # '135'

print(s[::-1])  # '54321'
print(s[4::-2])  # '531'
print(s[4:1:-2])  # '53'

# 注意
print(s[1:1])  # '' 不能切 开始索引和结束索引位置相同,结果为空字符串
print(s[3:1])  # '' 不能切 默认为正向切片,结果为空字符串
print(s[-len(s):1:-1])  # '' 起始和终止顺序为正向 切片为反向

print(s[:len(s)])  # '12345'
