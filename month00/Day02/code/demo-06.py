"""
    基础运算
"""
print(1 + 2)
print(1 - 2)
print(1 * 2)
print(1 / 2)

# ** 幂运算
print(2 ** 3)
# % 取余数
print(5 % 2)
# // 地板除 取商的整数部分
print(5 // 2)

# 特殊操作
# 1. 字符串 + 字符串： 表示拼接 将多个字符串进行拼接
class_info = "AIDTN2110" + "人工智能大家庭"
print(class_info)

# 2. 字符串 * 整数: 表示重复 将字符串的内容重复多次
desc = "我想涨薪！" * 3
print(desc)

