"""
    数学运算符
    成员运算
"""
# + 元素拼接
print("悟空" + "八戒")

# * 元素重复
print("悟空" * 2)

# == != > <　比较
# 依次比较两个容器中元素,一但不同则返回比较结果。
print("悟空" > "八戒")

# in  not in
print("悟空" in "花果山齐天大圣孙悟空")  # True
print("空悟" in "花果山齐天大圣孙悟空")  # False
print("天大" in "花果山齐天大圣孙悟空")  # True
print("山大" in "花果山齐天大圣孙悟空")  # False
